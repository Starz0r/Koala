namespace Koala.ViewModels
{
    /// <summary>
    /// Enumeration of a standard set of logging levels.
    /// </summary>
    public enum LogLevel
    {
        /// <summary>
        /// Debug message
        /// </summary>
        Debug = 0,
        /// <summary>
        /// Important informational message
        /// </summary>
        Notice = 2,
        /// <summary>
        /// Warning (potential error) message
        /// </summary>
        Warning = 3,
        /// <summary>
        /// Error message
        /// </summary>
        Error = 4
    }
}
